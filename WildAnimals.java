public class WildAnimals  extends Animals{
    private int inCage;
    private int count;
    private int startingInCage;
    private int time;

    public int getTime() { return time; }

    public void setTime(int time) { this.time = time; }

    public int getStartingInCage() { return startingInCage; }

    public void setStartingInCage(int startingInCage) { this.startingInCage = startingInCage; }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }

    public int getInCage() {
        return inCage;
    }

    public void setInCage(int inCage) {
        this.inCage = inCage;
    }
}
